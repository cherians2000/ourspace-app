/// OurSpace icon size scale.
///
/// Use these constants for every `Icon.size` and icon-driven layout so
/// icons stay visually consistent across features.
abstract final class AppIcons {
  /// Inline with captions and dense metadata.
  static const double xs = 16;

  /// Inline with body text, trailing icons in list tiles.
  static const double sm = 20;

  /// Default size: app bars, buttons, navigation.
  static const double md = 24;

  /// Feature emphasis: headers, prominent actions.
  static const double lg = 32;

  /// Empty states and illustrations.
  static const double xl = 48;
}
